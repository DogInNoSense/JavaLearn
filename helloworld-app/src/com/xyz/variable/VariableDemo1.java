package com.xyz.variable;

public class VariableDemo1 {
    public static void main(String[] args) {
        // 使用变量
        double money = 6.0;
        System.out.println(money);

        money = money + 4;
        System.out.println(money);

        System.out.println("------------------------------");
        int age = 21;
        System.out.println(age);
        age = 25;
        System.out.println(age);
    }
}
